package com.class7;

public class WhileLoopexercise {

	public static void main(String args[]) {
		int a= 50;
		
		while(a>=50&&a<=100) {
			if (a%2!=0) {
				System.out.println(a);
		}
		a++;
		}
		System.out.println("--------------------------------");
		
		int b=100;
		while (b>=50 && b <= 100) {
		  if (b%2 == 0) {
		  System.out.println(b);
	}
		  b--;
	}
	
}
}