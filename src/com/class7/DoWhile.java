package com.class7;

public class DoWhile {

	public static void main(String args[]) {
		
		int num=10;
		while(num<=7) {
			System.out.println("Hello");
			num++;
		}
		//rare
		int num1=10;
		do {
			System.out.println("Bye");
			num1++;
			
		}while(num1<=7);
		System.out.println("////////////////////////");
		}
	}

