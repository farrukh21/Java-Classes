package com.class31;

public class EncapsulationDemo {
	private String empName;
	  private int empAge;
	  
	  public void SetempName(String empName){
	    this.empName=empName;
	  }
	  public void SetempAge(int empAge){
	    this.empAge=empAge;
	  }
	 public String getempName(){
	    return empName;
	  }
	 public int getempAge(){
	    return empAge;
	  }
	  
	

}
